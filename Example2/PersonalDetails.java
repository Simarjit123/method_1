
public class PersonalDetails {
	int age;
	double height;
	double weight;

	PersonalDetails() {

	}

	PersonalDetails(int age) {
		this.age = age;

	}

	PersonalDetails(int age, double height) {
		this.age = age;
		this.height = height;

	}

	PersonalDetails(int age, double height, double weight) {
		this.age = age;
		this.height = height;
		this.weight = weight;
	}
}
